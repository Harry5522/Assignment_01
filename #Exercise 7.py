print("A loop that counts up from 0 to 50 in increments of 1.")
for i in range(0, 51):     #Prints numbers from 1 to 50
    print(i)


print("A loop that counts down from 50 to 0 in decrements of 1.")
for i_1 in range(50 ,-1, -1):    #It starts from 50 to 0 and in -1 second we tell it to minus one.
    print(i_1)


print("A loop that counts up from 30 to 50 in increments of 1.")
for i_2 in range(30, 51):   #Prints numbers from 30 to 50  
    print(i_2)


print("A loop that counts down from 50 to 10 in decrements of 2.")
for i_3 in range(50 ,9, -2):   #minus 2 from each operation until all numbers from 50 to 10 are printed.
    print(i_3)


print("A loop that counts up from 100 to 200 in increments of 5. ")
for i_4 in range(100, 205, 5):  #Add 5 from each operation until all numbers from 100 to 200 are printed.
    print(i_4)



